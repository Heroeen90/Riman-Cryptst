String translate(String key, String locale) {
  return i18nData[locale]?[key] ?? i18nData['en']?[key] ?? key;
}

String translateFormat(String key, String locale, Map<String, String> args) {
  String val = translate(key, locale);
  args.forEach((k, v) {
    val = val.replaceAll('{$k}', v);
  });
  return val;
}
